/*  Faça um programa que compare um número com 10 e imprime na tela "o número é maior, o número é menor o número é igual" */

let number = 9;

if (number > 10) {
    console.log('É maior.')
} else if (number == 10) {
    console.log('É igual.')
} else if (number < 10) {
    console.log('É menor.')
}